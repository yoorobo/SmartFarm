/**
 * PinConfig.h
 * ===========
 * ESP32 로봇 펌웨어 핀 배치 총괄 설정 파일.
 *
 * 모든 GPIO 핀 설정을 한곳에서 관리합니다.
 * 핀 변경 시 이 파일만 수정하면 됩니다.
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │                     ESP32 핀 배치도                          │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │  [모터 드라이버 - L298N]                                     │
 * │    ENA (PWM)  → GPIO 13    좌측 모터 속도                    │
 * │    IN1        → GPIO 14    좌측 모터 방향 1                  │
 * │    IN2        → GPIO 27    좌측 모터 방향 2                  │
 * │    IN3        → GPIO 26    우측 모터 방향 1                  │
 * │    IN4        → GPIO 25    우측 모터 방향 2                  │
 * │    ENB (PWM)  → GPIO 33    우측 모터 속도                    │
 * │                                                             │
 * │  [IR 라인 센서 - 5채널]                                      │
 * │    S1 (좌끝)  → GPIO 32                                     │
 * │    S2 (좌)    → GPIO 35                                     │
 * │    S3 (중앙)  → GPIO 34                                     │
 * │    S4 (우)    → GPIO 39                                     │
 * │    S5 (우끝)  → GPIO 36                                     │
 * │                                                             │
 * │  [RFID 리더 - MFRC522]                                      │
 * │    SCK        → GPIO 18    SPI 클럭                         │
 * │    MISO       → GPIO 21    SPI 데이터 입력                   │
 * │    MOSI       → GPIO 19    SPI 데이터 출력                   │
 * │    SDA/SS     → GPIO 5     Slave Select                    │
 * │    RST        → GPIO 4     리셋                             │
 * │    3.3V       → 3.3V       전원                             │
 * │    GND        → GND        접지                             │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 */

#ifndef PIN_CONFIG_H
#define PIN_CONFIG_H

// ═══════════════════════════════════════════════════════════════
//  모터 드라이버 핀 (L298N)
// ═══════════════════════════════════════════════════════════════

#define MOTOR_ENA_PIN   13      // 좌측 모터 PWM (속도 제어)
#define MOTOR_IN1_PIN   14      // 좌측 모터 방향 1
#define MOTOR_IN2_PIN   27      // 좌측 모터 방향 2
#define MOTOR_IN3_PIN   26      // 우측 모터 방향 1
#define MOTOR_IN4_PIN   25      // 우측 모터 방향 2
#define MOTOR_ENB_PIN   33      // 우측 모터 PWM (속도 제어)

// ═══════════════════════════════════════════════════════════════
//  IR 라인 센서 핀 (5채널)
// ═══════════════════════════════════════════════════════════════
//
//  센서 배치 (로봇 전면 기준):
//    S1(좌끝)  S2(좌)  S3(중앙)  S4(우)  S5(우끝)
//       ●       ●        ●        ●       ●
//
#define IR_S1_PIN       32      // 좌측 끝
#define IR_S2_PIN       35      // 좌측
#define IR_S3_PIN       34      // 중앙
#define IR_S4_PIN       39      // 우측
#define IR_S5_PIN       36      // 우측 끝

// ═══════════════════════════════════════════════════════════════
//  RFID 리더 핀 (MFRC522 - SPI 통신)
// ═══════════════════════════════════════════════════════════════

#define RFID_SCK_PIN    18      // SPI 클럭
#define RFID_MISO_PIN   21      // SPI Master In Slave Out
#define RFID_MOSI_PIN   19      // SPI Master Out Slave In
#define RFID_SS_PIN     5       // SPI Slave Select (SDA)
#define RFID_RST_PIN    4       // 리셋

// ═══════════════════════════════════════════════════════════════
//  예약된 핀 (사용하지 말 것)
// ═══════════════════════════════════════════════════════════════
//
//  GPIO 0  : 부트 모드 선택 (플래시 시 LOW)
//  GPIO 2  : 내장 LED (일부 보드)
//  GPIO 6-11: 내부 플래시 연결 (사용 금지)
//  GPIO 12 : 부트 모드 (MTDI)
//  GPIO 15 : 부트 모드 (MTDO)
//

// ═══════════════════════════════════════════════════════════════
//  사용 가능한 여유 핀
// ═══════════════════════════════════════════════════════════════
//
//  GPIO 13 : 범용 (PWM, ADC2, Touch)
//  GPIO 15 : 범용 (PWM, ADC2, Touch) - 부트 시 주의
//  GPIO 16 : 범용 (RX2)
//  GPIO 17 : 범용 (TX2)
//

#endif // PIN_CONFIG_H
